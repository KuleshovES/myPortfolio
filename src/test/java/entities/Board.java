package entities;

public class Board {
    public String id;
    String name;
    Boolean status;
    String idMemberCreator;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public Boolean getStatus() {
        return status;
    }

    public String getIdMemberCreator() {
        return idMemberCreator;
    }
}
